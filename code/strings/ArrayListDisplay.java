package strings;

import java.util.ArrayList;

public class ArrayListDisplay {

	public static void main(String[] args) {
		ArrayList<String> slist = new ArrayList<String>();
		slist.add("Beijing");
		slist.add("Shanghai");
		slist.add("Shenzhen");
		System.out.println(slist);
	}

}

/**Output:
[Beijing, Shanghai, Shenzhen]
*/
